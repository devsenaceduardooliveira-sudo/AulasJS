// Variaveis

// let é uma palavra reservada para criar variáveis,
// ou seja, um espaço na memória para armazenar do computador para
// armazenar um valor.

let animal = "Cachorro"; // A variável animal é declarada e inicializada com o valor "Cachorro"
console.log(animal); // Imprime o valor da variável animal no console

animal = "Gato"; // A variável animal é reatribuída com o valor "Gato"
console.log(animal); // Imprime o novo valor da variável animal no console

// const é uma palavra reservada para criar constantes,
// ou seja, um espaço na memória para armazenar do computador para
// armazenar um valor que não pode ser alterado.
const pi = 3.14; // A constante pi é declarada e inicializada com o valor 3.14
console.log(pi); // Imprime o valor da constante pi no console

pi = 3.14159;
