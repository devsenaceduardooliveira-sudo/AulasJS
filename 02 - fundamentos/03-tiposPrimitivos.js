// Tipos Primitivos

// String
// Number
// Boolean
// Null
// Undefined
// Symbol

// String: Representa uma sequência de caracteres
let nome = "Eduardo Oliveira";
let nome2 = "Adele";
let nome3 = "João";
let nome4 = `Maria`;

// Number: Representa números, tanto inteiros quanto decimais
let idade = 17;
let altura = 1.75;

// Boolean: Representa um valor lógico, que pode ser verdadeiro (true) ou falso (false)
let isStudent = true;
let isMarried = false;

let eEstudante = true;
let eCasado = false;

// Undefined: Representa uma variável que foi declarada,
// mas ainda não foi atribuída a um valor
let endereco;
let telefone;
console.log(endereco);

// Null: Representa a ausência intencional de um valor
let salario = null;

// Symbol: Representa um valor único e imutável,
// geralmente usado como identificador
let id = Symbol("id");
let id2 = Symbol("id");

console.log(nome);
console.log(nome2);
console.log(nome3);
console.log(nome4);
console.log(idade);
console.log(altura);
console.log(isStudent);
console.log(isMarried);
console.log(eEstudante);
console.log(eCasado);
console.log(salario);
console.log(id);
console.log(id2);
console.log(id === id2);
